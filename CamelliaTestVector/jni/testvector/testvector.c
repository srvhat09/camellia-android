void PrintBytes(unsigned char *data, int size, unsigned char *rtn) {
	unsigned char buf[10] = "";

	int i;
	for (i=0; i < size; i++) {
		sprintf(buf, "%02X ", data[i]);
		strcat((char *)rtn, (char *)buf);
	}
	return;
}

